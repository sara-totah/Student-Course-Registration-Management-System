    private void edit_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_edit_btnActionPerformed

        DefaultTableModel Df = (DefaultTableModel)courses_table.getModel();
        int selectedIndex = courses_table.getSelectedRow();
        
        try {
            int id = Integer.parseInt(Df.getValueAt(selectedIndex, 0).toString());
            
            String id_txt = course_id_txt.getText();
            String name_txt = course_name_txt.getText();
            String instructor_txt = course_instructor_txt.getText();
            String max_txt = course_max_txt.getText();
            String fees_txt = course_fees_txt.getText();
            
            Class.forName("com.mysql.jdbc.Driver");
            
             con1 = DriverManager.getConnection(url,username,password);
            insert = con1.prepareStatement("UPDATE courses SET course_name=?, instructor_name=?, max_students=?, course_fee=? WHERE course_id=?"); 
            
            insert.setInt(5, id);
            insert.setString(1, name_txt);
            insert.setString(2, instructor_txt);
            insert.setString(3, max_txt);
            insert.setString(4, fees_txt);

            insert.executeUpdate();
            
            JOptionPane.showMessageDialog(this,"Course Details Updated");
            table_update();
            
            //empty text fields after registering the student
            
            course_id_txt.setText("");
            course_name_txt.setText("");
            course_instructor_txt.setText("");
            course_max_txt.setText("");
            course_fees_txt.setText("");
            
            course_id_txt.requestFocus();
        }
        
        catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(course_enrollment.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_edit_btnActionPerformed

