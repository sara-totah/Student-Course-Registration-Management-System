    private void table_update(){
        
        int column_num;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            
            con1 = DriverManager.getConnection(url,username,password);
            insert = con1.prepareStatement("SELECT * FROM courses");         
            ResultSet rs = insert.executeQuery();
            
            ResultSetMetaData Rss = rs.getMetaData();
            column_num = Rss.getColumnCount();
            
            DefaultTableModel Df = (DefaultTableModel)courses_table.getModel();
            Df.setRowCount(0);
            
            while(rs.next())
            {
                Vector v2 = new Vector(); 
               
                for (int i=1; i <= column_num; i++){
                    v2.add(rs.getInt("course_id"));
                    v2.add(rs.getString("course_name"));
                    v2.add(rs.getString("instructor_name"));
                    v2.add(rs.getInt("max_students"));
                    v2.add(rs.getDouble("course_fee"));

                }
                
                Df.addRow(v2);
            }
        }
        
        catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(course_enrollment.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
